#write a method hash_sum that accepts a hash as arg and returns the total sum of all values of the hash
#You can assume the values of the hash are numbers.

# Examples
p hash_sum({"a"=>5, "b"=>2, "c"=>-1}) # => 6
p hash_sum({true=>40, 50=>3})         # => 43
p hash_sum({})                        # => 0