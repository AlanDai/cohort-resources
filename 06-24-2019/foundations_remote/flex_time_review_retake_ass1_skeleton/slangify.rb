# Write a method slangify that accepts a sentence string as an argument. 
# The method should return a new sentence where every word of the original sentence 
# has its first vowel removed. Vowels are the letters a, e, i, o, u.



# Examples

p slangify("follow the yellow brick road")  # => "fllow th yllow brck rad"
p slangify("try to stay awake please")      # => "try t sty wake plase"
p slangify("hey programmers")               # => "hy prgrammers"