# write a method, greatest_common_factor that accepts an array of positive numbers as an argument
# The method should return the greates common factor among all elements of the array. The greatest common factor is the largest number that 
# is able to divide all numbers of the array

# Examples
p greatest_common_factor([8, 24, 12])       # => 4 
p greatest_common_factor([15, 30])          # => 15
p greatest_common_factor([24, 30, 21])      # => 3
p greatest_common_factor([24, 30, 21, 7])   # => 1
p greatest_common_factor([16])              # => 16